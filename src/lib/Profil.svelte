                Lagenda Armada Sdn Bhd merupakan syarikat pemaju perumahan
dan perunding hartanah yang berlesen serta berdaftar dengan
Suruhanjaya Syarikat Malaysia (SSM). Ditubuhkan pada tahun 2015
dengan matlamat untuk terlibat di dalam industri pemajuan hartanah
dan binaan.<br/><br/>
Lagenda Armada Sdn Bhd bertanggungjawab dalam menjalankan
aktiviti hartanah sebagai pemaju untuk pembinaan projek perumahan
mampu milik. Lagenda Armada Sdn Bhd telah melaksanakan
beberapa projek pembinaan diantaranya projek Taman Delima Saujana,
Temerloh, Pahang, dan Taman D’Anggun Karak, Bentong, Pahang. 
Projek-projek pembinaan yang dijalankan oleh Lagenda Armada Sdn Bhd ini
lebih terarah kepada pembinaan rumah kediaman untuk kakitangan
awam melalui kelulusan daripada Pihak Berkuasa Tempatan (PBT) selaras
dengan peranan syarikat sebagai pemaju perumahan.<br/><br/>
Selain itu, Lagenda Armada Sdn Bhd juga terlibat dengan
perkhidmatan konsultasi am berkaitan pengurusan hartanah.
Perkhidmatan konsultasi yang ditawarkan adalah merangkumi aspek-aspek perundingan, jualan dan pengurusan.

